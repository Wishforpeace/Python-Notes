#3_1 列表是什么

#列表由一系列按特定顺序排列的元素组成。

#列表中的元素可以包含字母表中所有字母、数字0-9或所有家庭成员姓名等信息。

#也可以将任何东西加入列表中，其中的元素之间可以没有任何关系，列表元素也可以是不同的数据类型。

#鉴于列表通常包含多个元素，建议给列表指定一个表示复数的名称（如letters 、digits 或names ）。

#在Python中，用方括号（[]）来表示列表，并用逗号来分隔其中的元素。

bicycles = ['trek', 'cannondale', 'redline', 'specialized']  #列表bicycles的元素都是字符串
print(bicycles)

integers = [12, 18, 23, 34]  #列表integers的元素都是整数
print(integers)

decimals = [1.2, 1.8, 2.3, 3.4]  #列表decimals的元素都是小数
print(decimals)

records = ['trek', 18, 2.3]  #列表records的元素是不同数据类型的，有字符串，整数和小数
print(records)











