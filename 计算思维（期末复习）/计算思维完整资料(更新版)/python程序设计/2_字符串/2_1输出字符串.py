#2_1输出字符串

#后面的文字是注释语句，给语句提供解释，不影响语句执行

#字符串指的是字符组成的序列；
#在python中，字符串可以由单引号或双引号（还有三引号）括起来
#可以使用print语句输出字符串信息，将要输出的字符串放入print后面的括号里面

print('Hello Python world!')  #输出字符串Hello Python world!

print("Hello Python world!")  #也可以这样输出

print('''Hello Python world!''')  #也可以这样输出

#如果字符串由单引号括起来，这里面出现的双引号会原样输出
print('I told my friend, "Python is my favorite language!"')

#如果字符串由双引号括起来，这里面出现的单引号会原样输出
print("The language 'Python' is named after Monty Python")

